package jahirfiquitiva.apps.medicode.utils;

import android.content.Context;
import android.content.SharedPreferences;

import static android.content.Context.MODE_PRIVATE;

public class PreferencesUtils {
    public static void firstTimeAskingPermission(Context context, String permission, boolean
            isFirstTime) {
        SharedPreferences sharedPreference = context.getSharedPreferences("medicode",
                MODE_PRIVATE);
        sharedPreference.edit().putBoolean(permission, isFirstTime).apply();
    }

    public static boolean isFirstTimeAskingPermission(Context context, String permission) {
        return context.getSharedPreferences("medicode", MODE_PRIVATE).getBoolean(permission, true);
    }
}